// Problem
// Create a function that takes a number as an argument, increments the number by +1 and returns the result.
// Examples
// nextNumber(0) ➞ 1
// nextNumber(9) ➞ 10
// nextNumber(-3) ➞ -2

var myint = -1;
function nextNumber(myint){
    return myint + 1;
}
var myNextInt =  nextNumber(myint);
console.log(myNextInt);